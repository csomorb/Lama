        <p>
            Afin de fidéliser sa clientèle, Rossignol lance un nouveau programme de fidélité. J’ai été chargé de créer des pictogrammes illustrant les différents aspects de ce programme et de réfléchir à l’intégration de ces pictogrammes à différents supports. Ils sont présentés sur fond noir, rouge ou blanc, avec ou sans pastille, selon l’atmosphère que l’on souhaite véhiculer. En effet, les pictogrammes donnent une identité visuelle et définissent l’esprit du « Club ».
        </p>
